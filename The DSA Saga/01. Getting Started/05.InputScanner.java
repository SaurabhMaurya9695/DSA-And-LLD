// https://practice.geeksforgeeks.org/problems/java-basic-data-types0041/1

// Time: O(1) - Space O(1)

import java.util.*;

class Solution {
    int javaIntType(Scanner sc) {
        return sc.nextInt();
    }

    String javaStringType(Scanner sc) {
        return sc.next();
    }

    float javaFloatType(Scanner sc) {
        return sc.nextFloat();
    }
};